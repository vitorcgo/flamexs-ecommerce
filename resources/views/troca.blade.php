        <h1>TROCAS E DEVOLUÇÕES</h1>
        <h2>Como realizar uma troca?</h2>
        <p>Para solicitar a troca, entre em contato através do link https://flames.troque.app.br
            . Após confirmar se o pedido está dentro do prazo para troca, você receberá um código de postagem para nos enviar o produto de volta.</p>
        <p>Após a chegada do produto em nosso departamento de logística, será verificado se o produto se encontra nas condições para aceite de troca/devolução, caso seja aprovado, será disponibilizado um cupom no valor do produto para que uma nova compra possa ser realizada.</p>

        <p>Não fazemos reserva de peças, seja por modelo, cor ou tamanho. Por isso, não temos como garantir a disponibilidade da peça a ser trocada em estoque. Se a peça desejada não estiver disponível no momento da finalização do processo, o cliente poderá escolher outro item ou manter o cupom, uma vez que ele tem validade perpétua.</p>
        <p>A partir da segunda troca, o consumidor que arca com o custo do frete.</p>
        <p>Caso o valor do cupom não seja utilizado inteiramente, não há a possibilidade de estorno de possíveis saldos.</p>


        <h2>Como realizar uma devolução?</h2>
        <p>Aceitamos apenas devoluções por arrependimento solicitadas até 7 dias após o recebimento do produto, conforme as regras do CDC.</p>
        <p>Para solicitar a devolução, entre em contato através deste link https://flames.troque.app.br
            , informando seus dados, número do pedido e forma de estorno desejada. Após confirmarmos se o pedido está dentro do prazo para troca, você receberá um código de postagem para nos enviar o produto de volta.</p>
        <p>Após a chegada do produto em nosso departamento de logística, será verificado se o produto se encontra nas condições para aceite de troca/devolução, caso seja aprovado, o estorno será realizado através do mesmo meio de pagamento utilizado na compra.</p>

        <h2>Como será realizado o estorno do valor pago em caso de devolução?</h2>
        <p>O estorno de compras feitas com cartão de crédito será feito exclusivamente no mesmo cartão utilizado para a compra, e o crédito poderá ser identificado em até 2 faturas subsequentes à data de retorno do item para nós, dependendo da data de fechamento da mesma. No caso de compras feitas por boleto, o cliente deverá informar uma conta de mesma titularidade do comprador, para que o estorno seja feito.</p>

        <h2>Em que condições o produto deve estar para ser trocado ou devolvido?</h2>
        <p>O produto será analisado conforme regras do Código de Defesa do Consumidor, portanto o produto deve estar: <br>
            Em sua embalagem original com todos os adesivos, tags e lacres afixados. <br>
            Acompanhado de todos os acessórios recebidos.</p>

        <h3>Nao trocamos produtos com:</h3>
        <p>Desgaste <br>
            Sinais de uso como, mas não se restringindo a, manchas, odores ou rasgos <br>
            Danos causados por uso inadequado <br>
            Sinais de perfuração por objetos pontiagudos ou cortantes <br>
            Reparo/costuras <br>
            Qualquer alteração que culte as características originais do item <br>
            Ser esse o caso, o consumidor deve arcar com o custo do frete para que o produto volte a ele.
        </p>

        <h2>Recebi um produto com defeito, o que fazer?</h2>

        <p>Caso você receba um produto com defeito, pedimos que entre em contato conosco através do SAC no momento do recebimento do mesmo, e envie fotos que permitam identificar o defeito, para que possamos te atender rapidamente.</p>
